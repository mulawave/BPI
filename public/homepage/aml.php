                                <br>For any inquiries or reports related to this AML Policy, please contact our designated Compliance Officer:<br>
                                    <strong>Email:</strong> legals@beepagro.com
                                <br><br>
                                <strong>Conclusion</strong>
                                <br>BeepAgro Africa LTD is fully committed to combating money laundering and terrorist financing. We continually review and update our AML Policy to ensure compliance with evolving legal requirements and industry standards. By adhering to this policy, we aim to protect our business, clients, and the broader community from the risks associated with financial crimes.</p>

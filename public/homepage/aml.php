     						    <h3>Last Updated: 13th June 2025</h3><br>
     						    <h3>Anti-Money Laundering (AML) Policy for BeepAgro Africa LTD</h3><br>
     							<p><strong>Introduction</strong>
                                <br>BeepAgro Africa LTD is committed to the highest standards of integrity and ethical conduct in all its business activities. As part of this commitment, we have implemented a comprehensive Anti-Money Laundering (AML) Policy designed to prevent and detect any form of money laundering or terrorist financing activities. This policy aligns with international best practices and complies with the regulatory requirements of the Federal Republic of Nigeria, including those enforced by the Economic and Financial Crimes Commission (EFCC).
                                <br><br>
                                <strong>Purpose</strong>
                                <br>The purpose of this AML Policy is to ensure that BeepAgro Africa LTD:<br>
                                <ul><li>Complies with all relevant legal and regulatory obligations related to anti-money laundering.</li>
                                    <li>Implements effective measures to prevent the use of its products and services for money laundering or terrorist financing.</li>
                                    <li>Detect and report any suspicious activities to the appropriate authorities.</li></ul>
                                <br><br>
                                <strong>Scope</strong>
                                <br>This AML Policy applies to all employees, officers, directors, and third-party contractors of BeepAgro Africa LTD. It also extends to all clients, business partners, and other stakeholders involved in transactions with BeepAgro Africa LTD.
                                <br><br>
                                <strong>Key Policy Provisions</strong>
                                <br><strong>1.	Customer Due Diligence (CDD)</strong><br>
                                <ul><li><strong>Identification and Verification:</strong> We require all customers to provide valid identification documents before engaging in any business transactions. This includes, but is not limited to, government-issued IDs, proof of address, and any other relevant documentation.</li>
                                <li><strong>Enhanced Due Diligence (EDD):</strong> For high-risk customers or transactions, BeepAgro Africa LTD conducts Enhanced Due Diligence, which may include additional identification measures, closer scrutiny of transactions, and ongoing monitoring.</li></ul>
                                <br><strong>2.	Risk Assessment</strong><br>
                                    BeepAgro Africa LTD conducts regular risk assessments to identify, assess, and manage money laundering risks associated with its business activities. This includes evaluating customer profiles, transaction patterns, and geographical locations.<br>
                                <br><strong>3.	Transaction Monitoring</strong><br>
                                    We have implemented robust systems and procedures to monitor customer transactions for suspicious activities. These systems are designed to detect unusual or potentially illegal activities, which are promptly reported to the appropriate authorities.<br>
                                <br><strong>4.	Reporting Obligations</strong><br>
                                    BeepAgro Africa LTD complies with all reporting obligations under Nigerian law, including the timely filing of Suspicious Transaction Reports (STRs) with the EFCC. Employees are required to report any suspicious activity to the Compliance Officer, who is responsible for filing the STR.<br>
                                <br><strong>5.	Record Keeping</strong><br>
                                    All records related to customer identification, due diligence, and transaction monitoring are maintained for a minimum of five years, as required by law. These records are securely stored and made available to regulatory authorities upon request.<br>
                                <br><strong>6.	Training and Awareness</strong><br>
                                    BeepAgro Africa LTD provides regular training to all employees on AML policies, procedures, and legal obligations. This training ensures that employees are aware of their responsibilities and are equipped to identify and prevent potential money laundering activities.<br>
                                <br><strong>7.	Third-Party Relationships</strong><br>
                                    We ensure that all third-party contractors and business partners comply with our AML Policy. Due diligence is conducted on all third parties before entering into any business relationship.<br>
                                <br><strong>8.	Internal Controls and Audit</strong><br>
                                    BeepAgro Africa LTD has established internal controls and audit processes to ensure the effectiveness of its AML measures. Regular audits are conducted to assess compliance with this policy and to identify areas for improvement.</p>
                                <br><br>
                                <strong>EFCC Certification</strong>
                                <br>BeepAgro Africa LTD is proud to have received certification from the Economic and Financial Crimes Commission (EFCC), affirming our compliance with Nigerian AML regulations. This certification is a testament to our commitment to maintaining the highest standards of financial integrity.
                                <br><br>
